export function buildUserPrompt(input: string) {
  return `
아래 텍스트를 한국어로 번역하세요.

[회차/표식 규칙]
- "#1/#01" 같은 줄은 그대로 유지(번역 금지).
- "第1話" 같은 줄은 "제 1화"로 번역.
- 원문에 없는 회차/부제목 생성 금지.
- 회차/헤더 중복 출력 금지.

[원문]
${input}
`.trim();
}
