export function buildUserPrompt(input: string) {
  return `
아래 텍스트를 한국어로 번역하세요.

[회차 규칙]
- 원문이 "#1/#01"이면 결과는 "제 1화"로 번역.
- 원문이 "第1話"이면 결과는 "제 1화"로 번역.
- 원문이 이미 "제 1화"이면 그대로 유지.
- 회차 표식을 중복 출력하지 말 것.
- 원문에 부제목이 없으면 부제목을 만들지 말 것.

[원문]
${input}
`.trim();
}
